/*
TypeScript is a typed language.
However, it is not mandatory to specify the type of a variable.
TypeScript infers types of variables when there is no explicit information available in the form of type annotations.
*/

// TypeScript will infere that "mystring" should be a "string" type
let mystring = "some text";

// TypeScript will infere that "mynum" should be a "number" type
let mynum = 123;